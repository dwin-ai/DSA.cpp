#include <iostream>
using namespace std;

int main()
{
    //arthmetic operators
    cout<<"sum="<<(2+3)<<endl;
    cout<<"diff="<<(2-3)<<endl;
    cout<<"prod="<<(2*3)<<endl;
    cout<<"quo="<<(2/3)<<endl;
    cout<<"rem="<<(2%3)<<endl;
    
    //relational operators
    cout<<(2>3)<<endl;
    cout<<(2>=3)<<endl;
    cout<<(2<3)<<endl;
    cout<<(2<=3)<<endl;
    cout<<(2==3)<<endl;
    cout<<(2!=3)<<endl;
    
    
    //logical operators
    // or ||
    // and &&
    cout<<!(3<2)<<endl;
    cout<<(3<2)<<endl;
    
    return 0;
}
